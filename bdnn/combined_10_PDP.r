        delta <- 0.01 * abs(zlim[1])
        zlim[1] <- zlim[1] - delta
        zlim[2] <- zlim[2] + delta
      }
    }
  }
  if (is.matrix(temp$x) & is.matrix(temp$y) & is.matrix(temp$z)) {
    poly.grid <- TRUE
  }
  xthere <- match('x', names(temp))
  ythere <- match('y', names(temp))
  zthere <- match('z', names(temp))
